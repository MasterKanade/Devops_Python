course = "Python Programming"

# Print the length of the given string
print(len(course))
# Print the first letter of the string
print(course[0])
# Print the last letter of the string
print(course[-1])
# Print the first 3 letters of the string
print(course[0:3])
# Print the string if last index is not mentioned it will return the original string
print(course[0:])
# Print the Original string as it is
print(course[:])
# Print the string without start index : by default it will start from 0th index
print(course[:3])
